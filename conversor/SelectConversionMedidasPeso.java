    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String conversionSeleccionada = (String) jComboBox1.getSelectedItem();

        MensajeFinal mensajeFinal = new MensajeFinal();
        
        InputValorPeso inputValorPeso = new InputValorPeso();
        
        MasaInglesa gramos = new MasaInglesa();
		gramos.setValorOnza(0.0352);
		gramos.setValorLibra(0.0022);
        
        if(conversionSeleccionada == "De Onzas a Gramos"){
            
            double total = gramos.ConvertirDeOnzas(valorPeso);
        
            mensaje = "Son "+total+" gramos.";
            mensajeFinal.jLabel1.setText(mensaje);
           
        
    } else if(conversionSeleccionada == "De Libras a Gramos"){
            
         double total = gramos.ConvertirDeLibras(valorPeso);
        
            mensaje = "Son "+total+" gramos.";
            mensajeFinal.jLabel1.setText(mensaje);
           
            
        } else if(conversionSeleccionada == "De Gramos a Libras"){
            
            double total = gramos.ConvertirALibras(valorPeso);
        
            mensaje = "Son "+total+" libras.";
            mensajeFinal.jLabel1.setText(mensaje);
        
        } else if(conversionSeleccionada == "De Gramos a Onzas"){
            
            double total = gramos.ConvertirAOnzas(valorPeso);
        
            mensaje = "Son "+total+" onzas.";
            mensajeFinal.jLabel1.setText(mensaje);
           
            }
            
            mensajeFinal.setVisible(true);
            this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

